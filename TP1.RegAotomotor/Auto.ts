export class Auto {
    
    marca : string;
    tipo : string;
    anio : string;
    combustible : string;
    nMotor : string;
    nChasis : string;

    constructor(marca : string , tipo : string , anio : string , combustible : string ,nMotor : string , nChasis : string){
        this.marca = marca
        this.tipo = tipo
        this.anio = anio
        this.combustible = combustible
        this.nMotor = nMotor
        this.nChasis = nChasis
    }

}